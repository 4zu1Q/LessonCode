#include "ObstructFloat.h"

#define _USE_MATH_DEFINES
#include <math.h>

//-----------------------------------------------------------------------------
// @brief  コンストラクタ.
//-----------------------------------------------------------------------------
ObstructFloat::ObstructFloat():
	ObstructBase("data/model/obstructFloat/obstructFloat.pmx"),
	rad_(0)
{
	//処理なし
}

//-----------------------------------------------------------------------------
// @brief  デストラクタ.
//-----------------------------------------------------------------------------
ObstructFloat::~ObstructFloat()
{
}

//-----------------------------------------------------------------------------
// @brief  3Dモデルの描画.
//-----------------------------------------------------------------------------
void ObstructFloat::Draw()
{
	MV1DrawModel(modelHandle_);
}

//-----------------------------------------------------------------------------
// @brief  更新.
//-----------------------------------------------------------------------------
void ObstructFloat::Update()
{
	rad_ += static_cast<float>((M_PI / 180) * 5);

	pos_.y = 1.0f + sinf(rad_);

	// ３Dモデルのポジション設定
	MV1SetPosition(GetModelHandle(), GetPos());
}
